#ifndef __RAY_TYPES_H__
#define __RAY_TYPES_H__

typedef unsigned char		u8_t;		/*!< 8 bits unsigned char type */
typedef unsigned short		u16_t;		/*!< 16 bits unsigned short type */
typedef unsigned int		u32_t;		/*!< 32 bits unsigned int type */
typedef unsigned long long	u64_t;		/*!< 64 bits unsigned long long type */

typedef signed char			s8_t;		/*!< 8 bits signed char type */
typedef signed short		s16_t;		/*!< 16 bits signed short type */
typedef signed int			s32_t;		/*!< 32 bits signed int type */
typedef signed long long	s64_t;		/*!< 64 bits signed long long type */

typedef float				f32_t;		/*!< 32 bits float type */
typedef double				f64_t;		/*!< 64 bits double type */

typedef s32_t				fixed;		/*!< 32 bits signed fixed type */
typedef s64_t				fixed64;	/*!< 64 bits signed fixed type */

#endif
